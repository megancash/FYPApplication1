package com.example.fypapplication1.Notifications;

//Registration Token
public class Token {

    String token;

    //Empty constructor
    public Token() {
    }

    public Token(String token) {
        this.token = token;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }
}
